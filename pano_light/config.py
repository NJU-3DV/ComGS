import argparse
from omegaconf import OmegaConf

def load_config(config_path, base_config_path=None):
    """
    Load configuration parameters from a specified config file path.
    If a base configuration file path is provided, it merges the base configuration
    with the current configuration.

    Parameters
    ----------
    config_path : str
        Path to the configuration file. The file should be in YAML format.
    base_config_path : str, optional
        Path to the base configuration file. If provided, this file (also in YAML format)
        will be merged with the configuration from `config_path`. Defaults to None.

    Returns
    -------
    argparse.Namespace
        An object containing the configuration parameters as attributes.

    Raises
    ------
    FileNotFoundError
        If the specified `config_path` or `base_config_path` does not exist.
    omegaconf.errors.ConfigAttributeError
        If there are issues accessing or merging the configuration data using OmegaConf.
    yaml.YAMLError
        If the configuration files are not valid YAML format.

    Notes
    -----
    This function uses OmegaConf for loading and merging YAML configuration files.
    The merged configuration is converted to a dictionary and then set as attributes
    on an `argparse.Namespace` object for easy access.
    """
    config = OmegaConf.load(config_path)
    if base_config_path is not None:
        base_config = OmegaConf.load(base_config_path)
        config = OmegaConf.unsafe_merge(base_config, config)
    
    config_dict = OmegaConf.to_container(config, resolve=True)
    
    parser = argparse.ArgumentParser(description="Simple example of a ControlNet training script.")
    args = parser.parse_args([])
    for key, value in config_dict.items():
        setattr(args, key, value)
    
    return args

